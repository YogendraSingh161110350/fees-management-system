    // to validate all the field
    private boolean validationOfField()
    {
            fname = jTextField2.getText();
            lname = jTextField5.getText();
            username = jTextField1.getText();
            Con_password = jTextField3.getText();
            java.util.Date d1 = jDateChooser1.getDate();
            if(d1!=null)
            {
                datee = new Date(d1.getTime()); 
            }
            
            pass =String.valueOf(jPasswordField1.getPassword());
            mobile=jTextField4.getText();
            if(fname.length()<=2)
            {
                JOptionPane.showMessageDialog(rootPane, "invalid first name", "invalid name", JOptionPane.ERROR_MESSAGE);
                return false;
            }
            else if(lname.length()<=2)
            {
                JOptionPane.showMessageDialog(rootPane, "invalid last name", "invalid name", JOptionPane.ERROR_MESSAGE);
                return false;
            }
            else if(username.length()<=5)
            {
                JOptionPane.showMessageDialog(rootPane, "minimum 6 char required", "invalid username", JOptionPane.ERROR_MESSAGE);
                return false;
            }
            else if(pass.length()<=7)
            {
                JOptionPane.showMessageDialog(rootPane, "minimum 8 charcter required", "invalid password", JOptionPane.ERROR_MESSAGE);
                return false;
            }
            else if(!pass.equals(Con_password))
            {
                JOptionPane.showMessageDialog(rootPane, "password did not match", "password not match", JOptionPane.ERROR_MESSAGE);
                return false;
            }
            else if(datee==null)
            {
                JOptionPane.showMessageDialog(rootPane, "please select DOB", "invalid DOB", JOptionPane.ERROR_MESSAGE);
                return false;
            }
            else if(mobile.length()!=10)
            {
                JOptionPane.showMessageDialog(rootPane, "shold be 10 digits", "invalid mobile number", JOptionPane.ERROR_MESSAGE);
                return false;
            }
            else 
            {
                return true;
            }
    } 
