    // this method used to validate username and user password . it takes to parameter of String type.
    private boolean getValidation(String username,String pass)
    {
       try
       {
           Connection con = dbconnection.DBConnection.getConnection();
           PreparedStatement ps = con.prepareStatement("select * from signup where username=? and password=?");
           ps.setString(1, username);
           ps.setString(2, pass);
           ResultSet rs = ps.executeQuery();
           if(rs.next())
           {
               return true;
           }
       }
       catch(Exception e)
       {
           e.printStackTrace();
       } 
       return false;
    }
